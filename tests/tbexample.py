    def testkwargcheck1(self):
        """ test kwarg check on wcc_calc """
        self.createH(0.1, 0.3)
        # call to Z2Pack
        tb_system = z2pack.tb.System(self.H)
        tb_plane = tb_system.plane(1, 2, 0)
        self.assertRaises(
            TypeError,
            tb_plane.wcc_calc,
            invalid_kwarg = 3)

